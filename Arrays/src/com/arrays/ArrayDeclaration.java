package com.arrays;

public class ArrayDeclaration {

	public static void main(String[] args) {
		int[] a,b;//a and b has one dimensions
		int[] c[],d;//c has 2 dimensions and d has one dimension
		int[] e[],f[];//e and f has 2 dimensions
		int[] []g,h; //g and h has 2 dimensions
		int[] []i,j[]; //i has 2 dimensions and j has 3 dimensions
		//int[] []k,[]l; invalid declaration because if you want to specify dimension before the variable then the facility is applicable only for first variable
	}

}
