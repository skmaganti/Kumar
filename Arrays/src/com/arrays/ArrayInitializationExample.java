package com.arrays;

public class ArrayInitializationExample {

	public static void main(String[] args) {
		int[] x = new int[5];//By default array is initialized with default values
		x[0] = 10; //default values are overridden
		x[1] = 20;
		x[2] = 30;
		x[3] = 40;
		x[4] = 50;
		System.out.println(x[4]);
		
	}

}
