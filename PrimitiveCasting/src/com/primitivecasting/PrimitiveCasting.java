package com.primitivecasting;

public class PrimitiveCasting {

	public static void main(String[] args) {
		int a = 100;
		long b = a;
		float c = 100.0f;
		int d = (int)a;
		double e = 100L;
		int x = (int)456.34;
		long l = 150L;
		byte g = (byte)l;
		System.out.println(g);
		System.out.printf("%d",2.7f);
		
	}

}
